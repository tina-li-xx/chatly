import { readdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";

const root = process.cwd();
const libDir = path.join(root, "src", "lib");
const outputPath = path.join(libDir, "generated-blog-posts.ts");
const blogPostExport = /export const\s+([A-Za-z0-9_]+)\s*:\s*BlogPost\s*=/g;

function isBlogPostModule(fileName) {
  return fileName.startsWith("blog-post-") && fileName.endsWith(".ts");
}

async function readBlogPostModules() {
  const entries = await readdir(libDir, { withFileTypes: true });
  const modules = [];

  for (const entry of entries) {
    if (!entry.isFile() || !isBlogPostModule(entry.name)) {
      continue;
    }

    const fullPath = path.join(libDir, entry.name);
    const source = await readFile(fullPath, "utf8");
    const matches = [...source.matchAll(blogPostExport)];

    if (matches.length === 0) {
      continue;
    }

    if (matches.length > 1) {
      throw new Error(`Expected a single BlogPost export in ${entry.name}, found ${matches.length}.`);
    }

    modules.push({
      exportName: matches[0][1],
      fileName: entry.name
    });
  }

  return modules.sort((left, right) => left.fileName.localeCompare(right.fileName));
}

function buildRegistrySource(modules) {
  const importLines = modules.map(
    ({ exportName, fileName }) => `import { ${exportName} } from "./${fileName.replace(/\.ts$/, "")}";`
  );
  const postLines = modules.map(({ exportName }) => `  ${exportName},`);

  return [
    "// This file is auto-generated by scripts/generate-blog-registry.mjs.",
    "// Do not edit it by hand.",
    "",
    'import type { BlogPost } from "./blog-types";',
    ...importLines,
    "",
    "export const generatedBlogPosts: BlogPost[] = [",
    ...postLines,
    "];",
    ""
  ].join("\n");
}

async function main() {
  const modules = await readBlogPostModules();

  if (modules.length === 0) {
    throw new Error("No BlogPost exports were found in src/lib/blog-post-*.ts files.");
  }

  await writeFile(outputPath, buildRegistrySource(modules), "utf8");
  console.log(`Generated blog registry with ${modules.length} posts at ${path.relative(root, outputPath)}.`);
}

main().catch((error) => {
  console.error("generate-blog-registry failed", error);
  process.exit(1);
});
